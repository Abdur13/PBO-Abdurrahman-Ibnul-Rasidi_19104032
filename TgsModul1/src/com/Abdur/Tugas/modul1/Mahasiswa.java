public class Mahasiswa {
    String Nama,TTL,Alamat,Agama,JenisKelamin,PilJurusan,email,NoHP;
    int Nilai;

    //constractor
    public Mahasiswa(String Nama,String TTL,String Alamat,String Agama,String JenisKelamin,String PilJurusan,String email,String NoHP,int Nilai){
        this.Nama = Nama;
        this.TTL = TTL;
        this.Alamat = Alamat;
        this.Agama = Agama;
        this.JenisKelamin = JenisKelamin;
        this.PilJurusan = PilJurusan;
        this.email = email;
        this.Nilai = Nilai;
        this.NoHP = NoHP;
    }

}
